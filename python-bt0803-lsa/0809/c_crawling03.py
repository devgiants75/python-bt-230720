### 웹 크롤링 이해 ###

#! 1. 웹 크롤링
# : 웹 페이지에서 필요한 정보를 수집하고 선별하여 추출하는 과정을 의미
# : 웹 스크래핑(web scraping)이라고도 불림

#! 2. HTML의 개념
# HTML(HyperText Markup Language의 약자)
# 웹 페이지를 만드는 문법을 갖춘 언어

# HTML은 각자 역할이 정해진 태그(tag)들로 구성

#! 3. URL의 개념
# URL(Uniform Resource Locator)
# : 어떤 자원의 위치를 표기하는 방법을 의미

# URL 주소 표기 형식(규격)
# 프로토콜://도메인:포트번호/경로

# 예시
# https://github.com/devgiants75/python-bt-230720
# 프로토콜 (https): 하이퍼텍스트 문서를 전송할 때 사용하는 프로토콜(통신 규약)
# 도메인 (github.com): 깃허브 사이트 명(웹 서버의 주소)
# 포트 번호 (생략, 기본값: 80, 443)
# 경로 (/devgiants75/python-bt-230720): 서버 내에 특정 자원의 위치를 나타냄.