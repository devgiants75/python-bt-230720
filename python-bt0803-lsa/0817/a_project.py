### 주소록 프로그램 프로젝트 구현 ###

# CSV 파일로 관리하는 주소록 프로그램 구현
# 각 개인의 정보를 클래스를 기반으로 한 객체로 처리하는 객체지향 프로그래밍 방식으로 구현

# 프로그램이 시작되면 자동으로 CSV 파일 읽어오기
# 프로그램이 종료되면 변경된 내용이 CSV 파일에 반영

#! 프로젝트 개요
# 이름, 전화번호, 주소 정보를 포함하는 주소록
# 모든 데이터를 파일에 보관하는 방식으로 동작

#! 프로젝트 기능
# - 새로운 정보를 주소록에 등록
# - 기존 주소록 정보를 삭제
# - 기존 주소록 정보를 수정
# - 특정 주소록 정보를 검색
# - 전체 주소록 정보를 출력
# - 전체 주소록 정보를 파일로 관리