### 파이썬 예외 처리 ###

# 예외 & 오류의 정의

# 1. 예외(exception)란?
# : 프로그램에 존재하는 오류(error)를 의미
# : 예외가 발생하면 프로그램이 종료
# : 예외 처리를 하지 않으면 사용자에게 시스템 오류 메시지가 표시

# 2. 예외와 오류의 차이
# - 개발자가 직접 처리할 수 있는 문제: 예외
# - 개발자가 처리할 수 없는 문제: 오류

# 3. 예외 처리의 필요성

number = int(input("정수 입력 : ")) # 사용자 입력란
print(f'입력한 숫자는 {number}입니다.')
