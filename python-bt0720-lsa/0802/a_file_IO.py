# 3. with문
# : 파일을 열고 작업을 마친 후 자동으로 파일을 닫아주는 기능
# : close()함수를 따로 사용하지 않아도 가능

# 파일 객체 = open('파일명', 모드)
# with open('파일명', 모드) as file:
#   파일처리코드

with open('my_file.txt', 'wt') as file:
    print('my_file.txt 파일이 생성되었습니다.')