# 1-2. 모드 : 파일을 여는 목적
# 입력: r (read, 읽기) - 파일이 없을 경우(오류 발생), 파일이 있는 경우(읽기)
# - 입출력 모드를 생략하는 경우 기본적으로 r 모드로 파일이 열림.

# 출력: w (write, 쓰기) - 파일이 없을 경우(새로 생성), 파일이 있는 경우(새로 생성)
#      a (append, 추가) - 파일이 없을 경우(새로 생성), 파일이 있는 경우(추가)
#      x (exclusive, 배타적 추가) - 파일이 없을 경우(새로 생성), 파일이 있는 경우(오류 발생)

